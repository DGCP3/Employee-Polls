import React, { useState } from "react";
import PropTypes from "prop-types";
import { Container, Icon, Message } from "./styled";

const Alert = ({ children, type, title, hidden }) => {
  const [show, setShow] = useState(!hidden);
  const handleClose = () => {
    setShow(true);
  };
  const icon =
    type === "error" ? errorIcon : type === "warn" ? warningIcon : successIcon;

  return (
    <Container type={type} show={show}>
      <Icon>{icon}</Icon>
      <Message type={type}>
        <h1>{title && type}</h1>
        <p>{children}</p>
      </Message>
      <Icon as="closeButton" onClick={handleClose}>
        {closeIcon}
      </Icon>
    </Container>
  );
};
Alert.defaultProps = {
  type: "error",
  message: "",
};
Alert.propTypes = {
  type: PropTypes.oneOf(["success", "error", "warn"]),
  message: PropTypes.string,
};
export default Alert;

